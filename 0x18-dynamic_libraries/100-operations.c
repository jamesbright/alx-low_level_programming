/**
 * add - adds two numbers
 * @a: first number
 * @b: second number
 * Return: a + b
 */

int add(a, b)
{
int result = (int)a + b;
return (result);
}

/**
 * sub - subtracts two numbers
 * @a: first number
 * @b: second number
 * Return: a - b
 */

int sub(a, b)
{
int result = (int)a - b;
return (result);
}

/**
 * mul - multiplies two numbers
 * @a: first number
 * @b: second number
 * Return: a * b
 */

int div(a, b)
{
int result = (int)a / b;
return (result);
}

/**
 * mod - finds remainder of two numbers
 * @a: first number
 * @b: second number
 * Return: a % b
 */

int mod(a, b)
{
int result = (int)a % b;
return (result);
}

